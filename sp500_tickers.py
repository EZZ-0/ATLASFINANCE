def extract_ticker(display_string: str) -> str:
    """
    Extract ticker from 'Company Name (TICKER)' format
    
    Args:
        display_string: String in format "Company Name (TICKER)"
        
    Returns:
        Ticker symbol or empty string
    """
    if display_string == "--":
        return ""
    # Extract text between parentheses
    import re
    match = re.search(r'\(([^)]+)\)$', display_string)
    return match.group(1) if match else ""

